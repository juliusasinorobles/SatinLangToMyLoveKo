          <p><strong id="faq_1">Who are Underdog Idols?</strong> As the title eloquently says, there is more then just ONE idol. Record labels constantly seek new talent 
            to promote and turn into recording superstars. Whoever has the talent to make it in the music world, is definitely an idol. Don’t 
            let a handful of judges decide if you are good enough to make it in this industry, let millions of fans decide. Believe in yourself 
            and your dreams can become a reality.</p>

          <p><strong id="faq_2">How do I get started?</strong> You simply need to register as an Underdog, then make your video and post it on YouTube.com. 
            The final and most important step is then to post your Youtube video to the Underdog Idols site using the Youtube embedded code. The 
            rest is basically up to the fans. The ten artists with the most votes advance to the next round. From those ten, the fans will vote 
            again sending the three videos with the most votes in that round to be reviewed by record label executives. It is here where the magic 
            can happen. Most artists spend many years trying to get noticed, but now you have the opportunity in just one month, and you can enter month after month.</p>
          
          <p><strong id="faq_3">How does it all work?</strong> With Underdog Idols, the concept is a bit different. The first week of the month, registration 
            is open to everyone, that’s when you upload your video. If you miss that first week, don’t worry, you can still upload your video any 
            other week during that month, but it will NOT be available for votes until the next month. Entries received after the first week of the 
            month will be put in the category of "Upcoming Contestants" instead of "Current Contestants." At the beginning of the following month, 
            all videos in the "Upcoming Contestants" category will be moved to "Current Contestants" for that month along with all  the new videos 
            up-loaded between the 1st and the 7th of the current month. So basically, you can upload your video anytime and it will be in the contest 
            either that month or the next so you can relax and focus on creating a great quality video to upload. Take that time to create something AMAZING for the viewers.</p>
          
          <p><strong id="faq_4">What is the process after I upload my video?</strong> Once the tens of thousands of participants upload their videos to the 
            Underdog Idols’ website, they will be available for voting, either for a Thumbs Up or Thumbs Down. As soon as you upload your video, 
            you can promote yourself using social media, emails, your personal website, whatever you want to get as many votes as you can for the 
            remaining 3 weeks of the month. At the end of that month, our system will tally up the 10 top videos with the most votes and performers 
            will be notified by email through our Contestant’s Admin Panel as what to do next. You will then be advised to upload another video, 
            so you can compete to be in the top 3 that will have their videos sent to the participating recording label executives. Every month 
            has not ONE, but THREE winners! Those three winners will have their videos reviewed by real recording industry agents. So when you 
            make it to the top 10, we would suggest you contact our sponsors to get a professional video produced or photo shoot so you can 
            stand out and present yourself in the most professional manner. This would give you the opportunity to make a positive professional 
            impression on the music executives and possibly land you that recording contract you have been dreaming of.</p>
          
          <p><strong id="faq_5">What is the cost?</strong> You have seen the contestants on the popular TV reality shows, camp out for days to keep 
            their place in line, spend thousands of dollars in expenses and spend time away from their jobs and families just to be rejected 
            and sent home empty handed and heartbroken. With Underdog Idols, you participate from the comfort of your own home, without 
            spending a bunch of money and without being away from loved ones. The process will cost you no more then $10.00. If 
            you can afford to buy a couple slices of pizza and a soda, you can afford to be an Underdog Idol.</p>
          
          <p><strong id="faq_6">How can I get a professional video and photo shoot done?</strong> We thought of that too. We brought 
            professional photographers and videographers onboard to help you make a professional impression and get noticed. 
            These advertising partners are experienced in the entertainment industry and know what agents are looking for in a superstar. 
            We’ve dedicated a special section on the Underdog Idols homepage just for video production companies so they are easy for you 
            to find. They will help you produce the best videos so you can get as many votes as possible and get that much closer to your dreams. </p>
          
          <p><strong id="faq_7">If I get chosen by a recording company, what is the next step?</strong> Well, let us be the first to say, CONGRATS! 
            You are on your way to success. Once chosen, you will be put in contact with the agent or agents that are interested in you. 
            All agents and record labels have their own process for new artists, but they will most probably ask for a talent sheet, with 
            pictures, videos, and maybe a live audition. At that point, our job is done and you and the record executives will work together for any future deals. </p>
          
          <p><strong id="faq_8">Can I communicate with the record label executives and talent agents?</strong> Our system is structured so that when you 
            make it to the final top three artists with the most votes, the record executives come to you. Once you pass the second and last 
            round, your profile will be sent to recording label agents for review. They will then contact you for further information. 
            Once the initial contact is done, you will deal directly with the labels, and Underdog Idols will go back to making other 
            artists’ dreams a reality. Remember, all of the participating labels and agents are kept anonymous in order to protect their privacy.</p>

